from typing import Any, Dict, Tuple
from flask import request


DEFAULT_PAGE_SIZE = 20
MAX_PAGE_SIZE = 100


def get_pagination_params() -> Tuple[int, int]:
    try:
        page = int(request.args.get("page", 1))
    except ValueError:
        page = 1
    try:
        size = int(request.args.get("size", DEFAULT_PAGE_SIZE))
    except ValueError:
        size = DEFAULT_PAGE_SIZE
    size = max(1, min(size, MAX_PAGE_SIZE))
    page = max(1, page)
    return page, size


def make_pagination(page: int, size: int, total: int) -> Dict[str, Any]:
    pages = (total + size - 1) // size  # Calculate total pages (ceiling division)
    return {"page": page, "size": size, "total": total, "pages": pages}


def ok(data: Any = None, **kwargs):
    payload = {"data": data} if data is not None else {}
    payload.update(kwargs)
    return payload


def error(code: str, message: str, details: Any = None, trace_id: str | None = None):
    body: Dict[str, Any] = {"code": code, "message": message}
    if details is not None:
        body["details"] = details
    if trace_id:
        body["traceId"] = trace_id
    return body


def validate_health_record_data(data: Dict[str, Any]) -> Tuple[bool, str]:
    """
    Validate health record data according to business rules.
    Returns (is_valid, error_message).
    Generated by Copilot for issue #5
    """
    # Validate systolic blood pressure
    try:
        systolic = int(data.get("systolic"))
    except (TypeError, ValueError):
        return False, "收缩压必须为整数"
    
    if not (30 <= systolic <= 250):
        return False, "血压范围需在 30-250"
    
    # Validate diastolic blood pressure
    try:
        diastolic = int(data.get("diastolic"))
    except (TypeError, ValueError):
        return False, "舒张压必须为整数"
    
    if not (30 <= diastolic <= 250):
        return False, "血压范围需在 30-250"
    
    # Validate systolic > diastolic
    if systolic <= diastolic:
        return False, "收缩压必须大于舒张压"
    
    # Validate heart rate (optional)
    heart_rate = data.get("heart_rate")
    if heart_rate is not None:
        try:
            heart_rate = int(heart_rate)
        except (TypeError, ValueError):
            return False, "心率必须为整数"
        
        if not (30 <= heart_rate <= 150):
            return False, "心率范围需在 30-150"
    
    return True, ""
