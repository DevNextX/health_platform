"""
Simple test runner script. Generated by Zhuang
"""
import subprocess
import sys
import os

def run_tests():
    """Run the test suite"""
    try:
        # Change to project directory
        os.chdir(os.path.dirname(__file__))
        
        print("Installing test dependencies...")
        subprocess.run([sys.executable, "-m", "pip", "install", "pytest", "pytest-flask"], check=True)
        
        print("\nRunning tests...")
        result = subprocess.run([sys.executable, "-m", "pytest", "tests/", "-v", "--tb=short"], 
                              capture_output=False)
        
        if result.returncode == 0:
            print("\n✅ All tests passed!")
        else:
            print("\n❌ Some tests failed!")
            
        return result.returncode
        
    except subprocess.CalledProcessError as e:
        print(f"Error running tests: {e}")
        return 1
    except KeyboardInterrupt:
        print("\nTest run cancelled by user")
        return 1

if __name__ == "__main__":
    exit_code = run_tests()
    sys.exit(exit_code)
